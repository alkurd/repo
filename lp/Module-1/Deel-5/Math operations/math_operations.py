# example:
def increment(nr1: float) -> float:
  return nr1 + 1

def decrement(nr1:float) ->float:
  return nr1 -1
  

def add(nr1:float, nr2:float) -> float:
  return nr1 + nr2 

def subtract(nr1:float,nr2:float)->float:
  return nr1 - nr2


def multiply(nr1:float,nr2:float)->float:
  return nr1 * nr2


def divide(nr1:float, nr2: float)->float:
  if  nr2 == 0:
    return None
  else:
    return nr1 / nr2


